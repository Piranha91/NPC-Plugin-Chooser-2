namespace NPC_Plugin_Chooser_2.Tests.Unit;

/// <summary>
/// Newtonsoft round-trips through <see cref="JSONhandler{T}"/> for the persisted Models
/// (<see cref="ModSetting"/>, <see cref="NpcToken"/>, <see cref="NpcAppearanceData"/>).
/// The serializer settings (<c>JSONhandler&lt;T&gt;.GetMyMutagenJSONSettings</c>) register the
/// Mutagen converters (FormKey/ModKey &lt;-&gt; string), a StringEnumConverter
/// (enum as name), the read-only <c>ModSettingConverter</c> migration shim, and
/// <c>ObjectCreationHandling.Replace</c>. (See <c>JSONhandler.GetMyMutagenJSONSettings</c>.)
/// These tests assert the deterministic surface of the
/// models survives Serialize+Deserialize, that clones are reference-distinct, and that the
/// nullable <c>FormKey?</c> tuple element survives in both present and null forms.
/// Pure logic: no clock, no network, no game install.
/// </summary>
public class SerializableModelsRoundTripTests
{
    private static readonly FormKey Npc1 = FormKey.Factory("000801:Skyrim.esm");
    private static readonly FormKey Npc2 = FormKey.Factory("00ABCD:Skyrim.esm");
    private static readonly FormKey Npc3 = FormKey.Factory("0F1234:Dawnguard.esm");
    private static readonly ModKey ModA = ModKey.FromFileName("ModA.esp");
    private static readonly ModKey ModB = ModKey.FromFileName("ModB.esm");
    private static readonly ModKey ModC = ModKey.FromFileName("ModC.esl");

    // Round-trip via the production helpers, asserting both legs succeed.
    private static T RoundTrip<T>(T input)
    {
        var json = JSONhandler<T>.Serialize(input, out var serOk, out var serErr);
        serOk.Should().BeTrue("serialization should succeed: {0}", serErr);
        var clone = JSONhandler<T>.Deserialize(json, out var deOk, out var deErr);
        deOk.Should().BeTrue("deserialization should succeed: {0}", deErr);
        clone.Should().NotBeNull();
        return clone!;
    }

    // ── ModSetting ─────────────────────────────────────────────────────────────

    private static ModSetting MakeRichModSetting()
    {
        return new ModSetting
        {
            DisplayName = "Rich Mod",
            CorrespondingModKeys = new List<ModKey> { ModA, ModB },
            ResourceOnlyModKeys = new HashSet<ModKey> { ModC },
            CorrespondingFolderPaths = new List<string> { @"C:\Mods\Base", @"C:\Mods\Override" },
            MugShotFolderPaths = new List<string> { @"C:\Mugshots\Rich" },
            IsFaceGenOnlyEntry = true,
            FaceGenOnlyNpcFormKeys = new HashSet<FormKey> { Npc1 },
            IsAutoGenerated = false,
            NpcFormKeys = new HashSet<FormKey> { Npc1, Npc2, Npc3 },
            AvailablePluginsForNpcs = new Dictionary<FormKey, List<ModKey>>
            {
                [Npc1] = new() { ModA, ModB },
                [Npc2] = new() { ModC },
                [Npc3] = new(), // empty list must survive as empty (not dropped)
            },
            AmbiguousNpcFormKeys = new HashSet<FormKey> { Npc1 },
            NpcPluginDisambiguation = new Dictionary<FormKey, ModKey> { [Npc1] = ModB },
            NpcFormKeysToDisplayName = new Dictionary<FormKey, string> { [Npc2] = "Lydia" },
            MergeInDependencyRecords = false,
            HasAlteredMergeLogic = true,
            HandleInjectedRecords = true,
            HasAlteredHandleInjectedRecordsLogic = true,
            Keywords = new HashSet<string> { "follower", "vanilla" },
            ModRecordOverrideHandlingMode = RecordOverrideHandlingMode.IncludeAsNew,
            MaxNestedIntervalDepth = 5,
            IncludeAllOverrides = true,
            PluginsWithOverrideRecords = new HashSet<ModKey> { ModA },
            IncludeOutfits = true,
            CopyAssets = false,
            OverwriteBaseGameAssets = true,
            HasBaseGameAssetPaths = true,
            BaseGameAssetPathCount = 42,
            NpcFormKeysToNotifications = new Dictionary<FormKey,
                (NpcIssueType IssueType, string IssueMessage, FormKey? ReferencedFormKey)>
            {
                // present nullable FormKey
                [Npc1] = (NpcIssueType.Template, "templated to other NPC", Npc2),
                // null nullable FormKey
                [Npc2] = (NpcIssueType.Template, "no referenced key", null),
            },
            LastKnownState = new ModStateSnapshot
            {
                PluginSnapshots = { new FileSnapshot { FileName = "ModA.esp", FileSize = 1024, LastWriteTimeUtc = new DateTime(2026, 1, 1, 0, 0, 0, DateTimeKind.Utc) } },
                DirectorySnapshots = { new DirectorySnapshot { Path = @"C:\Mods\Base\Meshes", FileCount = 7, LastWriteTimeUtc = new DateTime(2026, 2, 2, 0, 0, 0, DateTimeKind.Utc) } },
            },
        };
    }

    [Fact]
    public void ModSetting_ScalarsAndEnum_SurviveRoundTrip()
    {
        var clone = RoundTrip(MakeRichModSetting());

        clone.DisplayName.Should().Be("Rich Mod");
        clone.IsFaceGenOnlyEntry.Should().BeTrue();
        clone.IsAutoGenerated.Should().BeFalse();
        clone.MergeInDependencyRecords.Should().BeFalse();
        clone.HasAlteredMergeLogic.Should().BeTrue();
        clone.HandleInjectedRecords.Should().BeTrue();
        clone.HasAlteredHandleInjectedRecordsLogic.Should().BeTrue();
        clone.MaxNestedIntervalDepth.Should().Be(5);
        clone.IncludeAllOverrides.Should().BeTrue();
        clone.IncludeOutfits.Should().BeTrue();
        clone.CopyAssets.Should().BeFalse();
        clone.OverwriteBaseGameAssets.Should().BeTrue();
        clone.HasBaseGameAssetPaths.Should().BeTrue();
        clone.BaseGameAssetPathCount.Should().Be(42);
        clone.ModRecordOverrideHandlingMode.Should().Be(RecordOverrideHandlingMode.IncludeAsNew);
    }

    [Fact]
    public void ModSetting_ModKeyAndFolderCollections_SurviveRoundTrip()
    {
        var clone = RoundTrip(MakeRichModSetting());

        clone.CorrespondingModKeys.Should().Equal(ModA, ModB);
        clone.ResourceOnlyModKeys.Should().BeEquivalentTo(new[] { ModC });
        clone.CorrespondingFolderPaths.Should().Equal(@"C:\Mods\Base", @"C:\Mods\Override");
        clone.MugShotFolderPaths.Should().Equal(@"C:\Mugshots\Rich");
        clone.FaceGenOnlyNpcFormKeys.Should().BeEquivalentTo(new[] { Npc1 });
        clone.NpcFormKeys.Should().BeEquivalentTo(new[] { Npc1, Npc2, Npc3 });
        clone.AmbiguousNpcFormKeys.Should().BeEquivalentTo(new[] { Npc1 });
        clone.PluginsWithOverrideRecords.Should().BeEquivalentTo(new[] { ModA });
        clone.Keywords.Should().BeEquivalentTo(new[] { "follower", "vanilla" });
    }

    [Fact]
    public void ModSetting_AvailablePluginsForNpcs_PreservesKeysAndOrderedValues()
    {
        var clone = RoundTrip(MakeRichModSetting());

        clone.AvailablePluginsForNpcs.Should().ContainKey(Npc1);
        clone.AvailablePluginsForNpcs[Npc1].Should().Equal(ModA, ModB);
        clone.AvailablePluginsForNpcs[Npc2].Should().Equal(ModC);
        // Empty value list must survive, not collapse to null or be dropped.
        clone.AvailablePluginsForNpcs.Should().ContainKey(Npc3);
        clone.AvailablePluginsForNpcs[Npc3].Should().NotBeNull().And.BeEmpty();
    }

    [Fact]
    public void ModSetting_NpcPluginDisambiguationAndDisplayNames_SurviveRoundTrip()
    {
        var clone = RoundTrip(MakeRichModSetting());

        clone.NpcPluginDisambiguation.Should().ContainKey(Npc1);
        clone.NpcPluginDisambiguation[Npc1].Should().Be(ModB);
        clone.NpcFormKeysToDisplayName.Should().ContainKey(Npc2);
        clone.NpcFormKeysToDisplayName[Npc2].Should().Be("Lydia");
    }

    [Fact]
    public void ModSetting_NotificationsTuple_PresentNullableFormKey_Survives()
    {
        var clone = RoundTrip(MakeRichModSetting());

        clone.NpcFormKeysToNotifications.Should().ContainKey(Npc1);
        var present = clone.NpcFormKeysToNotifications[Npc1];
        present.IssueType.Should().Be(NpcIssueType.Template);
        present.IssueMessage.Should().Be("templated to other NPC");
        present.ReferencedFormKey.Should().Be(Npc2);
        present.ReferencedFormKey.Should().NotBeNull();
    }

    [Fact]
    public void ModSetting_NotificationsTuple_NullNullableFormKey_Survives()
    {
        var clone = RoundTrip(MakeRichModSetting());

        clone.NpcFormKeysToNotifications.Should().ContainKey(Npc2);
        var nulled = clone.NpcFormKeysToNotifications[Npc2];
        nulled.IssueType.Should().Be(NpcIssueType.Template);
        nulled.IssueMessage.Should().Be("no referenced key");
        nulled.ReferencedFormKey.Should().BeNull();
    }

    [Fact]
    public void ModSetting_LastKnownState_SnapshotSurvivesAndCompactsEqual()
    {
        var original = MakeRichModSetting();
        var clone = RoundTrip(original);

        clone.LastKnownState.Should().NotBeNull();
        clone.LastKnownState!.PluginSnapshots.Should().ContainSingle();
        clone.LastKnownState.PluginSnapshots[0].FileName.Should().Be("ModA.esp");
        clone.LastKnownState.PluginSnapshots[0].FileSize.Should().Be(1024);
        clone.LastKnownState.DirectorySnapshots.Should().ContainSingle();
        clone.LastKnownState.DirectorySnapshots[0].FileCount.Should().Be(7);
        clone.LastKnownState.BsaSnapshots.Should().NotBeNull().And.BeEmpty();
        // The snapshot's own value-equality should agree the round-trip is faithful.
        clone.LastKnownState.Equals(original.LastKnownState).Should().BeTrue();
    }

    [Fact]
    public void ModSetting_Clone_IsReferenceDistinctFromOriginal()
    {
        var original = MakeRichModSetting();
        var clone = RoundTrip(original);

        clone.Should().NotBeSameAs(original);
        clone.CorrespondingModKeys.Should().NotBeSameAs(original.CorrespondingModKeys);
        clone.NpcFormKeys.Should().NotBeSameAs(original.NpcFormKeys);
        clone.AvailablePluginsForNpcs.Should().NotBeSameAs(original.AvailablePluginsForNpcs);
        clone.AvailablePluginsForNpcs[Npc1].Should().NotBeSameAs(original.AvailablePluginsForNpcs[Npc1]);
        clone.NpcFormKeysToNotifications.Should().NotBeSameAs(original.NpcFormKeysToNotifications);
        clone.LastKnownState.Should().NotBeSameAs(original.LastKnownState);

        // Mutating the clone must not bleed into the original.
        clone.CorrespondingModKeys.Add(ModKey.FromFileName("Extra.esp"));
        original.CorrespondingModKeys.Should().Equal(ModA, ModB);
    }

    [Fact]
    public void ModSetting_DefaultInstance_RoundTripsToEquivalentDefaults()
    {
        var clone = RoundTrip(new ModSetting());

        clone.DisplayName.Should().BeEmpty();
        clone.CorrespondingModKeys.Should().NotBeNull().And.BeEmpty();
        clone.ResourceOnlyModKeys.Should().NotBeNull().And.BeEmpty();
        clone.NpcFormKeys.Should().NotBeNull().And.BeEmpty();
        clone.AvailablePluginsForNpcs.Should().NotBeNull().And.BeEmpty();
        clone.NpcFormKeysToNotifications.Should().NotBeNull().And.BeEmpty();
        clone.MergeInDependencyRecords.Should().BeTrue();
        clone.CopyAssets.Should().BeTrue();
        clone.MaxNestedIntervalDepth.Should().Be(2);
        // ObjectCreationHandling.Replace: the default-init RecordOverrideHandlingMode.Ignore
        // (a non-null nullable) must be written and read back, not lost.
        clone.ModRecordOverrideHandlingMode.Should().Be(RecordOverrideHandlingMode.Ignore);
        clone.LastKnownState.Should().BeNull("no snapshot was assigned");
    }

    [Fact]
    public void ModSetting_NullOverrideHandlingMode_SurvivesAsNull()
    {
        var original = new ModSetting { ModRecordOverrideHandlingMode = null };
