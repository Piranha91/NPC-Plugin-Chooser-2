    // ── NpcToken ───────────────────────────────────────────────────────────────

    [Fact]
    public void NpcToken_FullGraph_SurvivesRoundTrip()
    {
        var token = new NpcToken
        {
            CreationDate = "2026-06-26T00:00:00Z",
            CreatedPlugins = new List<ModKey> { ModA, ModB, ModC },
            ProcessedNpcs = new Dictionary<FormKey, NpcAppearanceData>
            {
                [Npc1] = new() { ModName = "ModA", AppearancePlugin = ModA, OutputPlugin = ModB },
                [Npc2] = new() { ModName = "ModC", AppearancePlugin = ModC, OutputPlugin = ModB },
            },
        };

        var clone = RoundTrip(token);

        clone.CreationDate.Should().Be("2026-06-26T00:00:00Z");
        clone.CreatedPlugins.Should().Equal(ModA, ModB, ModC);
        clone.ProcessedNpcs.Should().HaveCount(2);
        clone.ProcessedNpcs.Should().ContainKey(Npc1);
        clone.ProcessedNpcs[Npc1].ModName.Should().Be("ModA");
        clone.ProcessedNpcs[Npc1].AppearancePlugin.Should().Be(ModA);
        clone.ProcessedNpcs[Npc1].OutputPlugin.Should().Be(ModB);
        clone.ProcessedNpcs[Npc2].AppearancePlugin.Should().Be(ModC);
    }

    [Fact]
    public void NpcToken_DictionaryFormKeyKeys_PreservedExactly()
    {
        var token = new NpcToken
        {
            ProcessedNpcs = new Dictionary<FormKey, NpcAppearanceData>
            {
                [Npc1] = new() { ModName = "A" },
                [Npc2] = new() { ModName = "B" },
                [Npc3] = new() { ModName = "C" },
            },
        };
        var clone = RoundTrip(token);

        clone.ProcessedNpcs.Keys.Should().BeEquivalentTo(new[] { Npc1, Npc2, Npc3 });
        // FormKey keys parse back into the same value type (deep, not stringly compared).
        clone.ProcessedNpcs.Should().ContainKey(Npc3);
        clone.ProcessedNpcs[Npc3].ModName.Should().Be("C");
    }

    [Fact]
    public void NpcToken_Clone_IsReferenceDistinctFromOriginal()
    {
        var token = new NpcToken
        {
            CreatedPlugins = new List<ModKey> { ModA },
            ProcessedNpcs = new Dictionary<FormKey, NpcAppearanceData>
            {
                [Npc1] = new() { ModName = "A", AppearancePlugin = ModA },
            },
        };
        var clone = RoundTrip(token);

        clone.Should().NotBeSameAs(token);
        clone.CreatedPlugins.Should().NotBeSameAs(token.CreatedPlugins);
        clone.ProcessedNpcs.Should().NotBeSameAs(token.ProcessedNpcs);
        clone.ProcessedNpcs[Npc1].Should().NotBeSameAs(token.ProcessedNpcs[Npc1]);

        clone.CreatedPlugins.Add(ModB);
        token.CreatedPlugins.Should().Equal(ModA);
    }

    [Fact]
    public void NpcToken_Empty_RoundTripsToEmpty()
    {
        var clone = RoundTrip(new NpcToken());
        clone.CreationDate.Should().BeEmpty();
        clone.CreatedPlugins.Should().NotBeNull().And.BeEmpty();
        clone.ProcessedNpcs.Should().NotBeNull().And.BeEmpty();
    }

    // ── CloneViaJSON convenience helper ────────────────────────────────────────

    [Fact]
    public void CloneViaJSON_ModSetting_ProducesEquivalentDistinctClone()
    {
        var original = MakeRichModSetting();
        var clone = JSONhandler<ModSetting>.CloneViaJSON(original);

        clone.Should().NotBeNull();
        clone.Should().NotBeSameAs(original);
        clone!.DisplayName.Should().Be(original.DisplayName);
        clone.NpcFormKeys.Should().BeEquivalentTo(original.NpcFormKeys);
        clone.AvailablePluginsForNpcs[Npc1].Should().Equal(original.AvailablePluginsForNpcs[Npc1]);
        clone.NpcFormKeysToNotifications[Npc1].ReferencedFormKey.Should().Be(Npc2);
        clone.NpcFormKeysToNotifications[Npc2].ReferencedFormKey.Should().BeNull();
    }

    [Fact]
    public void CloneViaJSON_NpcToken_ProducesEquivalentDistinctClone()
    {
        var token = new NpcToken
        {
            CreationDate = "x",
            CreatedPlugins = new List<ModKey> { ModA, ModB },
            ProcessedNpcs = new Dictionary<FormKey, NpcAppearanceData>
            {
                [Npc1] = new() { ModName = "A", AppearancePlugin = ModA, OutputPlugin = ModB },
            },
        };
        var clone = JSONhandler<NpcToken>.CloneViaJSON(token);

        clone.Should().NotBeNull();
        clone.Should().NotBeSameAs(token);
        clone!.CreatedPlugins.Should().Equal(ModA, ModB);
        clone.ProcessedNpcs.Should().NotBeSameAs(token.ProcessedNpcs);
        clone.ProcessedNpcs[Npc1].OutputPlugin.Should().Be(ModB);
    }
