    [Fact]
    public void ModSetting_EnumSerializedAsName_NotInteger()
    {
        var setting = new ModSetting { ModRecordOverrideHandlingMode = RecordOverrideHandlingMode.IncludeAsNew };
        var json = JSONhandler<ModSetting>.Serialize(setting, out var ok, out _);
        ok.Should().BeTrue();
        // StringEnumConverter is registered -> the enum is emitted by name.
        json.Should().Contain("IncludeAsNew");
        json.Should().NotContain("\"ModRecordOverrideHandlingMode\": 2");
    }

    [Fact]
    public void ModSetting_FormKeyAndModKey_SerializedAsStrings()
    {
        var setting = new ModSetting
        {
            CorrespondingModKeys = new List<ModKey> { ModA },
            NpcFormKeys = new HashSet<FormKey> { Npc1 },
        };
        var json = JSONhandler<ModSetting>.Serialize(setting, out var ok, out _);
        ok.Should().BeTrue();
        // Mutagen converters emit ModKey / FormKey as their canonical string forms.
        json.Should().Contain("ModA.esp");
        json.Should().Contain("000801:Skyrim.esm");
    }

    [Fact]
    public void ModSettingConverter_MigratesSingularMugShotFolderPath_ToList()
    {
        // The read-only ModSettingConverter migrates the legacy single-string property
        // "MugShotFolderPath" into the new "MugShotFolderPaths" list on deserialize.
        const string legacyJson = """
        {
          "DisplayName": "Legacy",
          "MugShotFolderPath": "C:\\Old\\Mugshots"
        }
        """;
        var migrated = JSONhandler<ModSetting>.Deserialize(legacyJson, out var ok, out var err);
        ok.Should().BeTrue("migration deserialize should succeed: {0}", err);
        migrated!.DisplayName.Should().Be("Legacy");
        migrated.MugShotFolderPaths.Should().ContainSingle().Which.Should().Be(@"C:\Old\Mugshots");
    }

    [Fact]
    public void ModSettingConverter_NewListWins_LegacySingularIgnoredWhenListPresent()
    {
        // When the new list already has entries, the legacy singular value is not merged in.
        const string mixedJson = """
        {
          "DisplayName": "Mixed",
          "MugShotFolderPath": "C:\\Old\\Mugshots",
          "MugShotFolderPaths": [ "C:\\New\\Mugshots" ]
        }
        """;
        var migrated = JSONhandler<ModSetting>.Deserialize(mixedJson, out var ok, out _);
        ok.Should().BeTrue();
        migrated!.MugShotFolderPaths.Should().Equal(@"C:\New\Mugshots");
    }

    [Fact]
    public void ModSettingConverter_LegacyMergeColor_SetsHasAlteredMergeLogic()
    {
        // Presence of the obsolete "MergeInLabelColor" implies the user altered merge logic.
        const string legacyJson = """
        {
          "DisplayName": "Colored",
          "MergeInLabelColor": "#FFAA0000"
        }
        """;
        var migrated = JSONhandler<ModSetting>.Deserialize(legacyJson, out var ok, out _);
        ok.Should().BeTrue();
        migrated!.HasAlteredMergeLogic.Should().BeTrue();
    }

